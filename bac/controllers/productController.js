exports.deleteReview = catchAsyncErrors(async (req, res, next) => {
  console.log("Query Params:", req.query);
  console.log("User from req.user:", req.user);

  const product = await Product.findById(req.query.productId);
  if (!product) {
    return next(new ErrorHander("Product not found", 404));
  }

  const reviewToDelete = product.reviews.find(
    (rev) => rev._id.toString() === req.query.id.toString()
  );

  if (!reviewToDelete) {
    return next(new ErrorHander("Review not found", 404));
  }

  if (reviewToDelete.user.toString() !== req.user._id.toString()) {
    return next(new ErrorHander("You can't delete this review", 403));
  }

  const reviews = product.reviews.filter(
    (rev) => rev._id.toString() !== req.query.id.toString()
  );

  let avg = 0;
  reviews.forEach((rev) => {
    avg += rev.rating;
  });

  const ratings = reviews.length === 0 ? 0 : avg / reviews.length;
  const numOfReviews = reviews.length;

  await Product.findByIdAndUpdate(
    req.query.productId,
    {
      reviews,
      ratings,
      numOfReviews,
    },
    {
      new: true,
      runValidators: true,
      useFindAndModify: false,
    }
  );

  res.status(200).json({ success: true });
});
