const Order = require("../models/orderModel");
const Product = require("../models/productModel");
const ErrorHander = require("../utils/errorhander");
const catchAsyncErrors = require("../middleware/catchAsyncErrors");

// Create new Order
// Create new Order (with duplicate prevention)
exports.newOrder = catchAsyncErrors(async (req, res, next) => {
  const {
    shippingInfo,
    orderItems,
    paymentInfo,
    itemsPrice,
    taxPrice,
    shippingPrice,
    totalPrice,
  } = req.body;

  console.log("🛠 Received Order Items:", orderItems);

  // ✅ Check if an order with the same payment ID already exists
  const existingOrder = await Order.findOne({
    "paymentInfo.id": paymentInfo.id,
    user: req.user._id, // ensure it's for the same user
  });

  if (existingOrder) {
    console.log("⚠️ Duplicate Order Prevented:", existingOrder._id);
    return res.status(200).json({
      success: true,
      message: "Order already exists with this payment ID.",
      order: existingOrder,
    });
  }

  const order = await Order.create({
    shippingInfo,
    orderItems,
    paymentInfo,
    itemsPrice,
    taxPrice,
    shippingPrice,
    totalPrice,
    paidAt: Date.now(),
    user: req.user._id,
  });

  console.log("✅ Order Created:", order._id);

  res.status(201).json({
    success: true,
    order,
  });
});


// get Single Order
exports.getSingleOrder = catchAsyncErrors(async (req, res, next) => {
  const order = await Order.findById(req.params.id).populate(
    "user",
    "name email"
  );

  if (!order) {
    return next(new ErrorHander("Order not found with this Id", 404));
  }

  res.status(200).json({
    success: true,
    order,
  });
});

